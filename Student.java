public class Student {

    private String name;
    private int id;
    private String department;
    private double gpa;
    private int year;

public Student(int id,String name,String department,double gpa,int year){
    this.name=name;
    this.id=id;
    this.department=department;
    this.gpa=gpa;
    this.year=year;  
}
}